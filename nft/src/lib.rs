#[derive(BorshSerialize, BorshStorageKey)]
enum StorageKey {
    NonFungibleToken,
    Metadata,
    TokenMetadata,
    Enumeration,
    Approval,
}

#[near_bindgen]
impl Contract {
    #[init]
    pub fn new_default_meta(owner_id: ValidAccountId) -> Self {
        Self::new(
            owner_id,
            NFTContractMetadata {
                spec: NFT_METADATA_SPEC.to_string(),
                name: "Example NEAR non-fungible token".to_string(),
                symbol: "EXAMPLE".to_string(),
                icon: Some(DATA_IMAGE_SVG_NEAR_ICON.to_string()),
                base_uri: None,
                reference: None,
                reference_hash: None,
            },
            3,
            1_000,
            "".to_string(),
        )
    }

    #[init]
    pub fn new(
        owner_id: ValidAccountId,
        metadata: NFTContractMetadata,
        mint_price: u32,
        max_supply: u32,
        base_url: String,
    ) -> Self {
        assert!(!env::state_exists(), "Already initialized");
        metadata.assert_valid();
        Self {
            tokens: NonFungibleToken::new(
                StorageKey::NonFungibleToken,
                owner_id,
                Some(StorageKey::TokenMetadata),
                Some(StorageKey::Enumeration),
                Some(StorageKey::Approval),
            ),
            metadata: LazyOption::new(StorageKey::Metadata, Some(&metadata)),
            mint_price,
            max_supply,
            minted_rational_pairs: LookupSet::new(b"s"),
            base_url,
        }
    }

    /// Mint a new token with ID=`token_id` belonging to `receiver_id`.
    ///
    /// Since this example implements metadata, it also requires per-token metadata to be provided
    /// in this call. `self.tokens.mint` will also require it to be Some, since
    /// `StorageKey::TokenMetadata` was provided at initialization.
    ///
    /// `self.tokens.mint` will enforce `predecessor_account_id` to equal the `owner_id` given in
    /// initialization call to `new`.
    #[payable]
    pub fn nft_mint(&mut self, r1: Rational, r2: Rational, receiver_id: ValidAccountId) -> Token {
        if self.tokens.owner_by_id.len() >= self.max_supply as u64 {
            panic!("Cannot mint more than {} tokens", self.max_supply);
        }

        let r1_simplified = r1.reduce();
        let r2_simplified = r2.reduce();

        if self
            .minted_rational_pairs
            .contains(&(r1_simplified.clone(), r2_simplified.clone()))
            || self
                .minted_rational_pairs
                .contains(&(r2_simplified.clone(), r1_simplified.clone()))
        {
            panic!("Cannot remint the same pair");
        }

        self.minted_rational_pairs
            .insert(&(r1_simplified.clone(), r2_simplified.clone()));

        let token_id = Self::make_title(&r1_simplified, &r2_simplified);
        self.tokens.mint(
            token_id.clone(),
            receiver_id,
            Some(TokenMetadata {
                title: Some(token_id),
                description: None,
                media: Some(self.make_media_url(&r1_simplified, &r2_simplified)),
                media_hash: None,
                copies: Some(1u64),
                issued_at: todo!(),
                expires_at: None,
                starts_at: None,
                updated_at: None,
                extra: None,
                reference: todo!(),
                reference_hash: None,
            }),
        )
    }
}

impl Contract {
    fn make_title(r1: &Rational, r2: &Rational) -> String {
        format!("{}/{} b{} -- {}/{} b{}", r1.n, r1.d, r1.b, r2.n, r2.d, r2.b)
    }

    // TODO:
    fn make_media_url(&self, r1: &Rational, r2: &Rational) -> String {
        format!(
            "{}/#{}/{} b{} -- {}/{} b{}",
            self.base_url, r1.n, r1.d, r1.b, r2.n, r2.d, r2.b
        )
    }
}

/// From Stack Overflow: https://codereview.stackexchange.com/questions/165283/fraction-type-in-rust
impl Rational {
    /// Returns a new Fraction that is equal to this one, but simplified
    pub fn reduce(&self) -> Self {
        // Use absolute value because negatives
        let gcd = gcd(self.n, self.d);
        Self {
            n: (self.n / gcd),
            d: (self.d / gcd),
            b: self.b,
        }
    }
}

/// From Stack Overflow: https://codereview.stackexchange.com/questions/165283/fraction-type-in-rust
// Calculate the greatest common denominator for two numbers
pub fn gcd(a: u32, b: u32) -> u32 {
    // Terminal cases
    if a == b {
        return a;
    }
    if a == 0 {
        return b;
    }
    if b == 0 {
        return a;
    }

    if a % 2 == 0 {
        // a is even
        if b % 2 != 0 {
            // b is odd
            return gcd(a / 2, b);
        } else {
            // a and b are even
            return gcd(a / 2, b / 2) * 2;
        }
    }

    // a is odd
    if b % 2 == 0 {
        // b is even
        return gcd(a, b / 2);
    }

    // Reduce larger argument
    if a > b {
        return gcd((a - b) / 2, b);
    }

    return gcd((b - a) / 2, a);
}

